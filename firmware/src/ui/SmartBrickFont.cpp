const Glyph *glyphFor(char c) {
  if (c < kFirstChar || c > kLastChar) {
    c = '?';
  }
  return &kGlyphs[(static_cast<uint8_t>(c) - kFirstChar) + 1];
}

bool glyphPixelOn(const Glyph &glyph, int x, int y) {
  if (x < 0 || y < 0 || x >= glyph.boxW || y >= glyph.boxH) {
    return false;
  }
  const int bit = y * glyph.boxW + x;
  const uint8_t byte = pgm_read_byte(&kBitmap[glyph.bitmapIndex + (bit >> 3)]);
  return (byte >> (7 - (bit & 7))) & 0x01;
}

